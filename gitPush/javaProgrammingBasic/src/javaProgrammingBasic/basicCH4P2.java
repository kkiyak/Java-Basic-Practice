package javaProgrammingBasic;

public class basicCH4P2 {
	public static void main(String[] args)
	{
	System.out.println("0-4는 " + (0-4));
	
	double d1 = 3.14;
	double d2 = d1*2;
	System.out.println("3.14x2는 " + d2);
	
	double d3 = 5;
	double d4 = 3;
	System.out.println("5/3은 " + d3/d4);
	
	System.out.println("30/7의 나머지 값은 "+(30%7));
	
	int d5 = 7;
	int d6 = 32;
	int d7=d5+d6;
	System.out.println("(7+32)/5는 " + (double)d7/5);
	
	}
}
